<h1 style="color: #E1B378; text-align: center;">Abridg Director User Guide</h1>
<p class="text">Abridg Director places you in the director&rsquo;s chair where you can:</p>
<ol class="text" style="margin-left: -18px;">
<li>Upload content (media files such as videos and images and virtually every type of document) via the <em>Upload</em> screen.</li>
<li>Add friends to share your content via the <em>Add</em> screen.</li>
<li>Assign your friends access to view your content via the <em>Assign</em> screen.</li>
<li>Perform housekeeping tasks on the <em>Manage</em> screen such as changing your password and managing alerts.</li>
</ol>

<hr style="border: 0px solid #000; height: 1px; color:#425A8D; background-color:#425A8D;" />

<h1 style="color: #9F0251;">1. The <em>Upload</em> Screen</h1>
<h3>Q. What&rsquo;s a Snapshot?</h3>
<p class="text">A. You&rsquo;ll only be invited to upload a snapshot if you&rsquo;re uploading a video. The snapshot is just an image file that you select to represent the video content &mdash; a little like an old-fashioned record sleeve or book cover. If your video features, say, highlights of your trip to Paris, you might select a picture of the Eiffel Tower as the accompanying snapshot. A video snapshot is often one frame from the video, but it can be any image. Whereas Abridg automatically scales snapshot images of any size, landscape images are generally best.</p>
<p class="text">Snapshots are optional. If you don&rsquo;t upload one with your video, Abridg will display your video using a generic snapshot. People will still be able to learn about the video without playing it from the title and description that you provided when you uploaded it.</p>

<h3>Q. Why is &lsquo;Assign Access&rsquo; optional on the <em>Upload</em> screen?</h3>
<p class="text">A. To save time, you can assign access to  friends as part of the upload process. Alternatively, you can defer assignment decisions until later, visiting the <em>Assign</em> screen when you&rsquo;re ready.</p>

<h3>Q. Can I close the upload progress monitor window during an upload?</h3>
<p class="text">A. Yes. Closing this window won&rsquo;t interfere with the upload process. (Don&rsquo;t close or navigate away from the window that displays the <em>Upload</em> page though.) When the upload process is complete, you&rsquo;ll see a confirmation message on your screen.</p>

<h3>Q. What types of file can I upload? How large?</h3>
<p class="text">A. Anything &mdash; well, almost. You can upload:</p>
<ul style="font-family: Geneva, Arial, Helvetica, sans-serif;">
<li>jpg, gif, png, and tiff image files</li>
<li>mp4, flv and f4v (Flash), mov video files</li>
<li>mp3 audio files</li>
<li>Word, Excel, PowerPoint, PDF, Photoshop, Adobe Illustrator, AutoCAD DXF, PostScript, EPS, XPS, and TTF documents</li></ul>
<p class="text">The maximum file size is 1 gigabyte (1000 MB)</p>

<h3>Q. Once uploaded, are my media items accessible on the public Internet?</h3>
<p class="text">A. Your media items are your own. The only people who&rsquo;ll be able to access them are friends to whom you&rsquo;ve assigned access. And each friend can only see the items you assigned him/her. Search engines won&rsquo;t be able to index your content and share it with the world. Abridg is about privacy and control.</p>

<hr style="border: 0px solid #000; height: 1px; color:#425A8D; background-color:#425A8D;" />

<h1 style="color: #9F0251;">2. The <em>Add</em> Screen</h1>
<h3>Q. After adding a friend, can I later edit the friend&rsquo;s name?</h3>
<p class="text">A. Yes. Visit the <strong>Assign</strong> page and click the &lsquo;Assign Media Items to Friend&rsquo; graphic. Then click the <img src="images/edit-icon.png" alt="Edit Icon" style="position: relative; top: 6px;"> [edit] icon next to the friend you wish to edit.</p>

<h3>Q. I want to delete a friend. How do I do that?</h3>
<p class="text">A. Visit the <strong>Assign</strong> page and click the <img src="images/delete-icon.png" alt="Delete Icon" style="position: relative; top: 6px;"> [delete] icon next to the friend.</p>

<h3>Q. What happens if my friend starts using a new email address?</h3>
<p class="text">A. You don&rsquo;t have to do anything. Your friend can change his/her email address (username) when he/she logs into Abridg account via the <strong>Manage</strong> menu. The change will take effect automatically. Alerts sent from you will now go to the new email address.</p>

<h3>Q. I want to add a friend who hasn&rsquo;t yet signed up for Abridg. Does that matter?</h3>
<p class="text">A. No. When you assign content to your friend, he/she will receive an email explaining how to view your content.</p>

<h3>Q. If I someone adds me as a friend, is that person now <em>my friend</em> too?</h3>
<p class="text">A. No. If Jack adds Jill as his friend, Jill will be able to view media items that Jack shares with her. But Jack won&rsquo;t be able to see anything of Jill&rsquo;s unless she adds Jack as her friend too. In the real world, relationships are sometimes like this &mdash; asymmetric. Abridg works the same way.</p>

<hr style="border: 0px solid #000; height: 1px; color:#425A8D; background-color:#425A8D;" />

<h1 style="color: #9F0251;">3. The <em>Assign</em> Screen</h1>
<h3>Q. What&rsquo;s the &ldquo;Sharelink&rdquo; web address that I see next to each of my media items?</h3>
<p class="text">A. Suppose your friend Jill wants to share an image or a video to which you&rsquo;ve assigned her access. She can share that file with her friend Tom simply by giving Tom the Sharelink web address. She needn&rsquo;t give Tom her username and password. Tom can simply visit the Sharelink page and access just that particular media item directly. He won&rsquo;t be able to view any other content from a Sharelink.</p>
<p class="text">Within the <em>Assign</em> screen, you can click on any Sharelink web address (example: www.abridg.com/qdm44zrjsnv) to open the corresponding Sharelink page. Each media item has a unique, encrypted Sharelink web address. Sharelink pages are completely inaccessible to anyone who hasn&rsquo;t been given the Sharelink.</p>

<h3>Q. How do I edit the name of a friend or delete a friend that I&rsquo;d previously added?</h3>
<p class="text">A. Click the &lsquo;Assign Media Items to Friend&rsquo; graphic on the <strong>Assign</strong> page. Then click the <img src="images/edit-icon.png" alt="Edit Icon" style="position: relative; top: 6px;"> [edit] icon next to the friend you wish to edit. Similarly, click the <img src="images/delete-icon.png" alt="Delete Icon" style="position: relative; top: 6px;"> [delete] icon to delete the friend.</p>

<h3>Q. How do I edit details of a media file or document that I&rsquo;d previously uploaded, or delete the file?</h3>
<p class="text">A. Click the &lsquo;Assign Friends to Media Item&rsquo; graphic on the <strong>Assign</strong> page. Then click the <img src="images/edit-icon.png" alt="Edit Icon" style="position: relative; top: 6px;"> [edit] icon next to the item you wish to edit. You&rsquo;ll be able to edit the file&rsquo;s title, description, capture date, and category. Similarly, click the <img src="images/delete-icon.png" alt="Delete Icon" style="position: relative; top: 6px;"> [delete] icon to delete the item.</p>

<h3>Q. Can I replace the actual media file while editing it?</h3>
<p class="text">A. No. Suppose you have a video file that you want to replace with a better quality version. The <em>Edit Media File</em> screen allows you to change the descriptive details of the file (for example, its Title, Description, Capture Date, and Snapshot image). But you can&rsquo;t edit or change the actual video file. To replace the file, you&rsquo;ll need to upload a new video via the <em>Upload</em> screen. You may also want to delete the original file by clicking the <img src="images/delete-icon.png" alt="Delete Icon" style="position: relative; top: 6px;"> [delete] icon on the Assign screen.</p>

<hr style="border: 0px solid #000; height: 1px; color:#425A8D; background-color:#425A8D;" />

<h1 style="color: #9F0251;">4. The <em>Manage</em> Screen</h1>
<h3>Q. If I destroy my account, are all my files deleted from Abridg?</h3>
<p class="text">A. Yes. Everything is deleted. If you later decide you want to use Abridg again, you&rsquo;ll have to upload your media items again.</p>

<h3>Q. What are Alerts?</h3>
<p class="text">A. Alerts are email messages to notify friends that you have new content (video, pictures, etc.) to share. If your friend doesn&rsquo;t yet have an Abridg account, the Alert will contain a link to instantly join Abridg and view your content with a single click. All he/she needs to do is confirm an email address and choose a password.</p>

<h3>Q. Can I control Alerts? I don&rsquo;t want to overload my friends with alert messages!</h3>
<p class="text">A. Yes you can and neither do we. Abridg&rsquo;s automated alert settings will never send an Alert to a friend more than once. Alternatively, if you&rsquo;re a control freak, you can manually control exactly when and to whom Alerts get sent.</p>

