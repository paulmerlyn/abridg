<h1 style="color: #9F0251;">FAQs</h1>

<h3>Q. I clicked &lsquo;New&rsquo; and nothing happened. Why not?</h3>
<p class="text">A. Clicking &lsquo;New&rsquo; reveals all media that have been uploaded by the account owner since you last logged in. If you see nothing, then nothing new has been uploaded.</p>

<h3>Q. How do I see images and videos in full-size?</h3>
<p class="text">A. Simply click once on the thumbnail image. The image or video will display in full-size. For larger files, you may experience a delay while the file loads.</p>

<h3>Q. What&rsquo;s this&nbsp;&nbsp;<img style="position: relative; top: 2px;" alt="Page Link Icon" src="/images/share-icon.png">&nbsp;&nbsp;&ldquo;Sharelink&rdquo; icon?</h3>
<p class="text">A. Suppose you want to share an image, a video, or some other file in your account with a friend or coworker, but you don&rsquo;t want to disclose your account password. Simply click the Sharelink icon. A new &ldquo;public&rdquo; (i.e. shareable) web page will open displaying that file. You can then share that page&rsquo;s web address to provide access to that file without granting anyone access to your account.</p>

<h3>Q. Can I print, download, and search documents on Abridg?</h3>
<p class="text">A. Easily. Click the <img style="position: relative; top: 2px;" alt="Page Link Icon" src="/images/share-icon.png">&nbsp;&nbsp;&ldquo;Sharelink&rdquo; icon to view the document in full. Then click the <img style="position: relative; top: 3px;" alt="Print-Save-Search Icon" src="/images/print-save-search-icon.png"> icon to access the &lsquo;<strong>File</strong>&rsquo; menu with options to print, download, and search the document.</p>

